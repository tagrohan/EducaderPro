    private String DATASTRC;
    private String MICRO;
    private String DLD;
    private String TUFFEL;
    private String DISCRETE;
    private String OS;
    private String MATHS3;
    private String HUMANVALUE;
    private String COA;

    public csecondpojo(){

    }


